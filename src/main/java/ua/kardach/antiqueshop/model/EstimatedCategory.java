package ua.kardach.antiqueshop.model;

public class EstimatedCategory {

	private long id;
	private String name;

	public EstimatedCategory() {

	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
